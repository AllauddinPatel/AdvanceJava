package Assignment;

public class Assignment32 {
    public static void main(String[] args) {


        for (int i=0;i<=10;i++){
            System.out.println(i);
        }
        //while loop
        System.out.println();
        int j=1;
        while (j<=10){
            System.out.println(j);
            j++;
        }

        //do while loop
        do {
            System.out.println(j);
            j++;
        }while (j<=10);
    }
}
